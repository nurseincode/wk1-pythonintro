# Data Types

# Numbers
# Int
# 5

# Finding the data type by calling the type function and passing the value. Output: <class 'int'>
print(type(5))

# Float
# 3.14159 pi approx

print(type(3.14159))

# Complex Numbers
print(type(2 + 3j))

# Number Operations
print(2 + 3)
print(2 - 3)
print(2 * 3) # Multiplication asteric symbol
print(10 / 5) # / division operator always returns a float, even if the result is a whole number. Output: 2.0
print(10 // 5) # Use the floor division operator: // if you want an integer result. Output: 2
